class A 
{
	public static void main(String[] args) 
	{
		//we can perform mathematical operations in the SOP
		//OPERAND[OPERATOR]OPERAND
		System.out.println((10 + 10) * (10 + 10));//20
		System.out.println(20 - 10);//10
		System.out.println(10 * 10);//100
		//In case of MOD or Modulus operator you will get remainder
		System.out.println(10 % 2);//0
		//In case of division you will get quotient 
		System.out.println(10 / 2);//5
	}
}
